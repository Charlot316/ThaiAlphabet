export type VocabularyExpansionCandidate = {
  thai: string;
  id: string;
  roman: string;
  chinese: string;
  partOfSpeech: VocabularyExpansionPartOfSpeech;
  theme: VocabularyExpansionTheme;
  level: "a1" | "a2";
  priority: number;
  senses: VocabularyExpansionSense[];
  synonyms: VocabularyExpansionRelatedWord[];
  antonyms: VocabularyExpansionRelatedWord[];
  comparisons: VocabularyExpansionComparison[];
  collocations: VocabularyExpansionCollocation[];
  usageNotesZh: string[];
  tags: string[];
  sourceRefs: string[];
  reviewStatus: "candidate-draft";
};

type CandidateSeed = Omit<
  VocabularyExpansionCandidate,
  | "senses"
  | "synonyms"
  | "antonyms"
  | "comparisons"
  | "collocations"
  | "usageNotesZh"
  | "sourceRefs"
  | "reviewStatus"
> & {
  senseId: string;
  senseChinese: string;
  example: VocabularyExpansionExample;
  synonyms?: VocabularyExpansionRelatedWord[];
  antonyms?: VocabularyExpansionRelatedWord[];
  comparisons?: VocabularyExpansionComparison[];
  collocations?: VocabularyExpansionCollocation[];
  usageNotesZh?: string[];
  sourceRefs?: string[];
};

const LEXICON_REFS = ["thai-frequency", "wiktionary-thai-frequency", "thaipod101-core100"];

const candidate = (entry: CandidateSeed): VocabularyExpansionCandidate => {
  const synonyms = entry.synonyms ?? [];
  const antonyms = entry.antonyms ?? [];
  const comparisons = entry.comparisons ?? [];
  const collocations = entry.collocations ?? [];
  const usageNotesZh = entry.usageNotesZh ?? [];

  return {
    thai: entry.thai,
    id: entry.id,
    roman: entry.roman,
    chinese: entry.chinese,
    partOfSpeech: entry.partOfSpeech,
    theme: entry.theme,
    level: entry.level,
    priority: entry.priority,
    senses: [
      {
        id: entry.senseId,
        chinese: entry.senseChinese,
        examples: [entry.example],
        synonyms,
        antonyms,
        comparisons,
        collocations,
        usageNotesZh,
        tags: entry.tags,
      },
    ],
    synonyms,
    antonyms,
    comparisons,
    collocations,
    usageNotesZh,
    tags: entry.tags,
    sourceRefs: entry.sourceRefs ?? LEXICON_REFS,
    reviewStatus: "candidate-draft",
  };
};

