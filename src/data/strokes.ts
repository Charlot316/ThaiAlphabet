function componentSlotTarget(componentKey: string, index: number, count: number): TargetBox {
  const slotW = 100 / count;
  const slotX = slotW * index;
  const box = SLOT_COMPONENT_BOX[componentKey] ?? { y: 32, h: 36, wScale: 0.75 };
  const w = Math.max(6, slotW * box.wScale);
  return {
    x: slotX + (slotW - w) / 2,
    y: box.y,
    w,
    h: box.h,
  };
}

function pathBounds(d: string): Bounds | null {
  const segments = parsePathSegments(d);
  if (!segments?.length) return null;
  const points = segments.flatMap((segment) =>
    segment.cmd === "L"
      ? [segment.from, segment.to]
      : [segment.from, segment.c1, segment.c2, segment.to]
  );
  const xs = points.map((point) => point.x);
  const ys = points.map((point) => point.y);
  return {
    minX: Math.min(...xs),
    minY: Math.min(...ys),
    maxX: Math.max(...xs),
    maxY: Math.max(...ys),
  };
}

function transformPoint(point: Point, bounds: Bounds, target: TargetBox): Point {
  const sourceW = Math.max(bounds.maxX - bounds.minX, 1);
  const sourceH = Math.max(bounds.maxY - bounds.minY, 1);
  const scale = Math.min(target.w / sourceW, target.h / sourceH);
  const offsetX = target.x + (target.w - sourceW * scale) / 2;
  const offsetY = target.y + (target.h - sourceH * scale) / 2;
  return {
    x: offsetX + (point.x - bounds.minX) * scale,
    y: offsetY + (point.y - bounds.minY) * scale,
  };
}

function transformPath(d: string, bounds: Bounds, target: TargetBox): string {
  const segments = parsePathSegments(d);
  if (!segments?.length) return d;
  return pathSegmentsToD(
    segments.map((segment) =>
      segment.cmd === "L"
        ? {
            cmd: "L",
            from: transformPoint(segment.from, bounds, target),
            to: transformPoint(segment.to, bounds, target),
          }
        : {
            cmd: "C",
            from: transformPoint(segment.from, bounds, target),
            c1: transformPoint(segment.c1, bounds, target),
            c2: transformPoint(segment.c2, bounds, target),
            to: transformPoint(segment.to, bounds, target),
          }
    )
  );
}

function transformStroke(stroke: Stroke, bounds: Bounds, target: TargetBox): Stroke {
  const sourceD = stroke.sourceD ?? stroke.d;
  return {
    ...stroke,
    d: transformPath(stroke.d, bounds, target),
    sourceD: transformPath(sourceD, bounds, target),
    points: stroke.points?.map((point) => ({
      id: point.id,
      ...transformPoint({ x: point.x, y: point.y }, bounds, target),
    })),
    sequence: stroke.sequence ? [...stroke.sequence] : undefined,
  };
}

/** Compute the union of all stroke bounds for a component so multi-stroke
 *  marks (e.g. ◌ ึ = ellipse + small circle in the corner) keep their
 *  relative sizes/positions when scaled into a slot. */
function componentBounds(strokes: Stroke[]): Bounds | null {
  let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
  let any = false;
  for (const stroke of strokes) {
    const b = pathBounds(stroke.sourceD ?? stroke.d);
    if (!b) continue;
    any = true;
    if (b.minX < minX) minX = b.minX;
    if (b.minY < minY) minY = b.minY;
    if (b.maxX > maxX) maxX = b.maxX;
    if (b.maxY > maxY) maxY = b.maxY;
  }
  return any ? { minX, minY, maxX, maxY } : null;
}

function placeComponent(componentKey: string, component: LetterStrokes, target: TargetBox | null): LetterStrokes {
  const shouldKeepGuides = componentKey === "v:placeholder";
  if (!target) {
    return {
      ...component,
      strokes: component.strokes.map(cloneStroke),
      guides: shouldKeepGuides ? component.guides?.map(cloneStroke) : undefined,
    };
  }
  // Use the union of all strokes (and guides for placeholder) so every stroke
  // shares the same scale/offset; otherwise small marks like the corner dot of
  // ◌ ึ get rescaled to fill the slot and look like a second ellipse.
  const allForBounds = shouldKeepGuides
    ? [...component.strokes, ...(component.guides ?? [])]
    : component.strokes;
  const bounds = componentBounds(allForBounds);
  if (!bounds) {
    return {
      ...component,
      strokes: component.strokes.map(cloneStroke),
      guides: shouldKeepGuides ? component.guides?.map(cloneStroke) : undefined,
    };
  }
  return {
    ...component,
    strokes: component.strokes.map((stroke) => transformStroke(stroke, bounds, target)),
    guides: shouldKeepGuides ? component.guides?.map((stroke) => transformStroke(stroke, bounds, target)) : undefined,
  };
}

function baseLetterStrokes(key: string): LetterStrokes | null {
  if (key === "v:placeholder") return PLACEHOLDER_STROKES;
  const mappedKey = VOWEL_TO_CONSONANT[key] || key;
  return LETTER_STROKES[mappedKey] ?? null;
}

export function getVowelStrokeComponentKeys(key: string): string[] | null {
  return VOWEL_STROKE_COMPONENTS[key] ?? null;
}

/**
 * Components that visually overlay (above/below) the previous main component.
 * They share the same horizontal slot as the preceding main component instead
 * of taking their own slot.
 */
const OVERLAY_COMPONENTS = new Set([
  "v:mai-han-akat",
  "v:mai-kham-mark",
  "v:i-short",
  "v:i-long",
  "v:ue-short",
  "v:ue-long",
  "v:u-short",
  "v:u-long",
]);

export function composeLetterStrokes(
  key: string,
  resolve: (componentKey: string) => LetterStrokes | null = baseLetterStrokes
): LetterStrokes | null {
  const componentKeys = getVowelStrokeComponentKeys(key);
  if (!componentKeys) return null;

  const components = componentKeys.map(resolve);
  if (components.some((component) => !component)) return null;

  // Compute slot index for each component. Overlay components reuse the
  // previous main component's slot so e.g. ◌ื sits centered above the
  // placeholder circle instead of being shoved into the next slot.
  const mainCount = componentKeys.filter((k) => !OVERLAY_COMPONENTS.has(k)).length;
  const slotCount = Math.max(1, mainCount);
  let lastMainSlotIndex = 0;
  let nextMainSlotIndex = 0;
  const slotIndices = componentKeys.map((k) => {
    if (OVERLAY_COMPONENTS.has(k)) return lastMainSlotIndex;
    lastMainSlotIndex = nextMainSlotIndex;
    return nextMainSlotIndex++;
  });

  const resolvedComponents = (components as LetterStrokes[]).map((component, index) => {
    const componentKey = componentKeys[index];
    return placeComponent(componentKey, component, componentSlotTarget(componentKey, slotIndices[index], slotCount));
  });

  const strokes = resolvedComponents.flatMap((component) => component.strokes.map(cloneStroke));
  const guides = resolvedComponents.flatMap((component) => component.guides?.map(cloneStroke) ?? []);

  return {
    v: Math.max(...resolvedComponents.map((component) => component.v), 5),
    strokes,
    guides: guides.length > 0 ? guides : undefined,
  };
}

export function getLetterStrokes(key: string): LetterStrokes | null {
  return baseLetterStrokes(key);
}
